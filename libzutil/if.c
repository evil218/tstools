/* from uint8_t buffer to "xx xx ... xx xx, \0" */
int b2t(char *DST, const uint8_t *SRC, int len)
{
        int i;
        const char *ch;
        char *dst = DST;
        const uint8_t *src = SRC;

        for(i = 0; i < len - 1; i++) {
                ch = b2t_table[*src++];
                *dst++ = *ch++;
                *dst++ = *ch;
                *dst++ = ' ';
        }

        ch = b2t_table[*src++];
        *dst++ = *ch++;
        *dst++ = *ch;
        *dst++ = ',';
        *dst++ = ' ';

        *dst++ = '\0';
        return 0;
}

/* match ".*\*.*,?", change ',' to '\0', stop at '?' */
int next_tag(char **tag, char **text)
{
        char ch;

        /* search head('*') */
        do {
                ch = *(*text)++;
        } while(('*' != ch) &&
                ('\0' != ch) && ('\n' != ch) && ('\r' != ch));

        if('*' == ch) {
                /* tag head */
                *tag = *text - 1;
        }
        else {
                /* normal EOL */
                (*text)--;
                *(*text) = '\0';
                return NEOL;
        }

        /* search tail(',' or '\033') */
        do {
                ch = *(*text)++;
                if('\033' == ch) {
                        /* tag tail, it is color tags */
                        *(*text - 1) = '\0';
                }
        } while((',' != ch) &&
                ('\0' != ch) && ('\n' != ch) && ('\r' != ch));

        if(',' == ch) {
                /* tag tail */
                *(*text - 1) = '\0';
                return 0; /* *text point to ' ' after ',' */
        }
        else {
                /* unexpected EOL */
                (*text)--;
                *(*text) = '\0';
                return UEOL;
        }
}

/* match " XX XX ... XX XX,?", stop at ? */
int next_nbyte_hex(uint8_t *byte, char **text, int max)
{
        int cnt;

        for(cnt = 0; cnt < max; cnt++) {
                uint8_t s, h, l;

                /* white space */
                s = *(*text);
                if('\0' == s || '\n' == s || '\r' == s) {
                        break;
                }
                if(',' == s) {
                        /* data end */
                        (*text)++;
                        break;
                }
                (*text)++;

                /* hi */
                h = *(*text);
                if('\0' == h || '\n' == h || '\r' == h) {
                        break;
                }
                (*text)++;

                /* lo */
                l = *(*text);
                if('\0' == l || '\n' == l || '\r' == l) {
                        break;
                }
                (*text)++;

                *byte++ = (t2b_table_h[h] | t2b_table_l[l]);
        }

        return cnt;
}

/* match " XX...XX,?", stop at ? */
int next_nuint_hex(long long int *sint, char **text, int max)
{
        int cnt;

        for(cnt = 0; cnt < max; cnt++) {
                uint8_t s, x;
                unsigned long long int dat = 0;

                /* white space */
                s = *(*text);
                if('\0' == s || '\n' == s || '\r' == s) {
                        break;
                }
                if(',' == s) {
                        /* array end */
                        (*text)++;
                        break;
                }
                (*text)++;

                while(1) {
                        x = *(*text);
                        if('\0' == x || '\n' == x || '\r' == x) {
                                break;
                        }
                        else if(('0' <= x) && (x <= '9')) {
                                dat = (dat << 4) + (x - '0');
                        }
                        else if(('A' <= x) && (x <= 'F')) {
                                dat = (dat << 4) + (x - 'A' + 10);
                        }
                        else if(('a' <= x) && (x <= 'f')) {
                                dat = (dat << 4) + (x - 'a' + 10);
                        }
                        else {
                                /* data end */
                                *sint++ = (long long int)dat;
                                break;
                        }
                        (*text)++;
                }
        }

        return cnt;
}
